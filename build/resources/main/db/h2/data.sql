    COMPANY_TYPE_ID bigint AUTO_INCREMENT primary key,
    TYPE varchar
);

CREATE TABLE MEMBER (
    ID bigint AUTO_INCREMENT PRIMARY KEY,
    NAME varchar(255),
    PASSWORD varchar(255),
    SEX varchar(255),
    COMPANY_NAME varchar(255),
    COMPANY_LOCATION_ID bigint,
    COMPANY_TYPE_ID bigint
);

